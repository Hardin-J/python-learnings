# for loop iterates over a sequence

numbers = [10, 20, 30, 40]

total = 0
for num in numbers:          # Iterates one value at a time
    total += num             # Adds each value to total

print("Total:", total)

# while loop runs until condition becomes False
count = 1
while count <= 5:
    print("Count:", count)
    count += 1
