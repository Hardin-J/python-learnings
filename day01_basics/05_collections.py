# List (Mutable, Ordered)
marks = [70, 80, 90]
marks.append(100)
print(marks)

# Tuple (Immutable, Ordered)
coordinates = (10, 20)
print(coordinates)

# Dictionary (Key-Value Pair)
student = {
    "name": "Jeeva",
    "age": 21,
    "course": "AI"
}

print(student["name"])   # Access using key
print(student["age"])
